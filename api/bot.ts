	const callbackData = ctx.callbackQuery.data
	await ctx.answerCallbackQuery() // –í—Å–µ–≥–¥–∞ –æ—Ç–≤–µ—á–∞–π—Ç–µ –Ω–∞ callback-–∑–∞–ø—Ä–æ—Å—ã

